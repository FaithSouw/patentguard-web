                      zkBindingKey:zk.bindingKey,zkSalt:zk.salt,proofType:zk.proofType,
                      publicationSchedule:sched.id,publicationMonths:sched.months,
                      timestamp:ts,submittedAt:new Date().toISOString(),
                      status:"SUBMITTED",source:"user_portal",tokenData:td,
