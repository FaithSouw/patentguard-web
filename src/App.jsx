async function ledgerAdd(entry) {
  try {
    const idx=await store.get("ledger:index",true).catch(()=>null);
    const index=idx?JSON.parse(idx.value):[];
    if(!index.includes(entry.id)) index.push(entry.id);
    await store.set(`ledger:${entry.id}`,JSON.stringify(entry),true);
    await store.set("ledger:index",JSON.stringify(index),true);
    return true;
  } catch(e){console.error(e);return false;}
}
async function ledgerGetAll() {
  try {
    const idx=await store.get("ledger:index",true).catch(()=>null);
    if(!idx) return [];
    const ids=JSON.parse(idx.value);
    const entries=await Promise.all(ids.map(id=>store.get(`ledger:${id}`,true).then(r=>r?JSON.parse(r.value):null).catch(()=>null)));
    return entries.filter(Boolean).sort((a,b)=>b.timestamp-a.timestamp);
  } catch {return [];}
}
async function ledgerVerify(id) {
  try {
    const r=await store.get(`ledger:${id}`,true);
    if(!r) return {verified:false,reason:"Not found in ledger"};
    const entry=JSON.parse(r.value);
    const result=await verifyLedgerEntry(entry);
    return {...result, entry};
  } catch(e){return {verified:false,reason:e.message};}
}

