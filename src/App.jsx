async function searchUSPTO(query,perPage=30) {
  try {
    const res=await fetch(`${API_BASE}/uspto/search?q=${encodeURIComponent(query)}&per_page=${perPage}`,{headers:FORM_HEADERS});
    if(!res.ok) throw new Error("backend");
    return (await res.json()).patents||[];
  } catch {
    const p=new URLSearchParams({q:JSON.stringify({_text_any:{patent_abstract:query}}),f:JSON.stringify(["patent_id","patent_title","patent_abstract","patent_date","assignee_organization"]),o:JSON.stringify({size:perPage,sort:[{patent_date:"desc"}]})});
    const r=await fetch(`https://search.patentsview.org/api/v1/patent/?${p}`,{headers:{Accept:"application/json"}});
    const d=await r.json();
    return (d.patents||[]).map(p=>({id:`US${p.patent_id}`,title:p.patent_title||"",abstract:(p.patent_abstract||"").slice(0,400),year:p.patent_date?parseInt(p.patent_date):null,assignee:p.assignee_organization||"—",url:`https://patents.google.com/patent/US${p.patent_id}`,source:"uspto"})).filter(p=>p.abstract.length>40);
  }
}
async function callClaude(messages,system) {
  // Calls the backend proxy (/claude) so the Anthropic API key stays server-side.
  const resp=await fetch(`${API_BASE}/claude`,{method:"POST",headers:FETCH_HEADERS,body:JSON.stringify({system,messages})});
  if(!resp.ok) return "";
  const d=await resp.json();
  return d.text||d.content?.find(b=>b.type==="text")?.text||"";
}
