function semanticScore(inputText, patent) {
  const iW=inputText.toLowerCase().split(/\W+/).filter(w=>w.length>3);
  const pW=(patent.title+" "+patent.abstract).toLowerCase().split(/\W+/).filter(w=>w.length>3);
  const iS=new Set(iW),pS=new Set(pW);
  let ex=0; for(const w of iS) if(pS.has(w)) ex++;
  const iC=new Set(iW.map(getCluster).filter(Boolean));
  const pC=new Set(pW.map(getCluster).filter(Boolean));
  let sc=0; for(const c of iC) if(pC.has(c)) sc++;
  return Math.min(1,(ex/Math.max(iS.size,1))*0.55+(sc/Math.max(Math.max(iC.size,pC.size),1))*0.65);
}

