const SW=new Set(["the","a","an","and","or","but","in","on","at","to","for","of","with","by","from","is","are","was","were","be","been","have","has","had","do","does","did","will","would","could","should","may","might","this","that","these","those","it","its","not","no","as","than","more","some","any","all","so","very","just","also","then","when","where","which","who","what"]);
function tokenize(raw) {
  if(!raw?.trim()) return {tokens:[],topKeywords:[],bigrams:[],clusters:[],sections:[],claims:[],stats:{}};
  const t0=performance.now();
  const clean=raw.replace(/[^\x20-\x7E\n]/g," ").replace(/\s+/g," ").trim();
  const sents=clean.split(/(?<=[.!?])\s+(?=[A-Z])/).filter(s=>s.length>10);
  const all=clean.toLowerCase().split(/\W+/).filter(w=>w.length>2);
  const toks=all.filter(w=>!SW.has(w));
  const freq={}; for(const t of toks) freq[t]=(freq[t]||0)+1;
  const topKeywords=Object.entries(freq).sort((a,b)=>b[1]-a[1]).slice(0,25).map(([word,count])=>({word,count}));
  const bg={}; for(let i=0;i<toks.length-1;i++){const k=`${toks[i]} ${toks[i+1]}`;bg[k]=(bg[k]||0)+1;}
  const bigrams=Object.entries(bg).filter(([,c])=>c>1).sort((a,b)=>b[1]-a[1]).slice(0,12).map(([phrase,count])=>({phrase,count}));
  const cm={}; for(const t of toks){const c=getCluster(t);if(c)cm[c]=(cm[c]||0)+1;}
  const clusters=Object.entries(cm).sort((a,b)=>b[1]-a[1]).slice(0,8).map(([name,count])=>({name,count}));
  const sRe={Abstract:/\babstract\s*[:\-\n]/i,Background:/\bbackground\s*[:\-\n]/i,Claims:/\bclaims?\s*[:\-\n]/i,Summary:/\bsummary\s*[:\-\n]/i};
  const sections=Object.entries(sRe).filter(([,re])=>re.test(clean)).map(([n])=>n);
  const claims=sents.filter(s=>/\bwherein\b|\bcomprising\b|\bconsisting\b/i.test(s)).slice(0,8);
  return {clean:clean.slice(0,4000),tokens:toks,topKeywords,bigrams,clusters,sections,claims,stats:{chars:raw.length,words:all.length,uniqueWords:Object.keys(freq).length,sentences:sents.length,tokens:toks.length,claims:claims.length,processMs:(performance.now()-t0).toFixed(1)}};
}

