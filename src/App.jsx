// ── Submit to government pending queue ───────────────────────────────────────
// Writes to shared pending:* storage so GovPortal can read it immediately
async function submitToGovQueue(submission) {
  try {
    const idx   = await store.get("pending:index", true);
    const index = idx ? JSON.parse(idx.value) : [];
    if (!index.includes(submission.id)) index.push(submission.id);
    await store.set(`pending:${submission.id}`, JSON.stringify(submission), true);
    await store.set("pending:index", JSON.stringify(index), true);
    return true;
  } catch(e) { console.error("submitToGovQueue error:", e); return false; }
}

